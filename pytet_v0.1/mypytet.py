def draw_matrix(m):
    array = m.get_array()
    for y in range(m.get_dy()):
        for x in range(m.get_dx()):
            if array[y][x] == 0:
                print("□", end='')
            elif array[y][x] == 1:
                print("■", end='')
            else:
                print("XX", end='')
        print()

def rotate_matrix(m, clockwise):
    array = m.get_array()
    N = len(array)
    ret = [[0] * N for _ in range(N)]
    for r in range(N):
        for c in range(N):
            if(clockwise == 1):
                ret[c][N-1-r] = array[r][c]
            elif(clockwise == -1):
                ret[r][c] = array[c][N-1-r]
            else:
                return Matrix(m)
    return Matrix(ret)


