def draw_matrix(m):
    array = m.get_array()
    for y in range(m.get_dy()):
        for x in range(m.get_dx()):
            if array[y][x] == 0:
                print("□", end='')
            elif array[y][x] == 1:
                print("■", end='')
            else:
                print("XX", end='')
        print()

def rotate_matrix(m):
    N = len(m)
    ret = [[0] * N for _ in range(N)]
    for r in range(N):
        for c in range(N):
            ret[c][N-1-r] = m[r][c]
    return ret

def create_rotated_blk(m, times):
    N = len(m)
    array = [m]
    for i in range(times):
        temp = []
        for j in range(N):
            temp.append(rotate_matrix(array[i][j]))
        array.append(temp)
    return array
